#include <iostream>
#include <conio.h>

//--------------------------------------------------------------------------------------------------------------
// PretzelWatcherApp
//--------------------------------------------------------------------------------------------------------------

class PretzelWatcherApp {
public:
  int run();
};

//--------------------------------------------------------------------------------------------------------------
// PretzelWatcherApp
//--------------------------------------------------------------------------------------------------------------

int PretzelWatcherApp::run() {
  printf("--- Pretzel Watcher ---\n\n");

  bool finished = false;

  printf("Press 'q' to quit.\n");

  while (!finished) {
    if (_kbhit()) {
      char c = _getch();

      if (c == 'q')
        finished = true;
    }

    Sleep(100);
  }

  return 0;
}

//--------------------------------------------------------------------------------------------------------------
// main
//--------------------------------------------------------------------------------------------------------------

int main(int argc, char* ppArgv[]) {
  PretzelWatcherApp app;

  return app.run();
}
